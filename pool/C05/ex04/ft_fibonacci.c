int	ft_fibonacci(int index)
{
	int	res;

	res = 0;
	if (index < 0)
		return (-1);
	if (index == 0)
		return (0);
	else if (index == 1)
		return (1);
	else
	{
		res += ft_fibonacci(index - 1) + ft_fibonacci (index - 2);
		return (res);
	}
}
