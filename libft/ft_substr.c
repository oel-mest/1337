#include "libft.h"

char	*ft_substr(const char *s, unsigned int start, size_t len)
{
	char			*substr;
	size_t			s_len;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		len = 0;
	else if (len > s_len - start)
		len = s_len - start;
	substr = (char *)malloc (len + 1);
	if (!substr)
		return (NULL);
	if (len > 0)
		ft_strlcpy(substr, s + start, len + 1);
	else
		substr[0] = '\0';
	return (substr);
}
